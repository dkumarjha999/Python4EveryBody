n=input("enter a number")
n=int(n)
for i in range(1,n+1):
    print(i)
for i in range(0,n+1,2): # third argument is how much step increment of i
    print(i)
for i in range(1,n+1,2):
    print(i)


for i in range(n,0,-2):  # how much decrementation
    print(i)
for i in range(n-1,0,-2):
    print(i)

